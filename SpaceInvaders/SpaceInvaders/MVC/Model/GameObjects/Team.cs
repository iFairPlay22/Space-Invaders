namespace SpaceInvaders.GameObjects
{
    /// <summary>
    /// Determines the team of an object
    /// PLAYER is the team of the players
    /// ENNEMY is the team of the players ennemies
    /// NEUTRAL is the team of an object that don't fight any objects
    /// </summary>
    public enum Team
    {
        PLAYER,
        ENNEMY,
        NEUTRAL
    }
}
